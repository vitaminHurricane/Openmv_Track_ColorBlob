/* USER CODE BEGIN 1 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if(htim == &htim4) {
    static uint16_t cnt = 0;
    cnt++;
    if (cnt >= 10) {
      /************************云台定时旋转角度调节***************************/
      if (cur_angle_up != aim_up) {       //当当前角度不符合期望角度时
        cur_angle_up += change_up;        //每次让当前角度向期望角度靠拢change的量
        if (fabs(cur_angle_up - aim_up) <= 3) {
          DuoJi_SetAngle(UP, aim_up);
          cur_angle_up = aim_up;
        } else {
          DuoJi_SetAngle(UP, cur_angle_up);
        }
      } 
      if (cur_angle_down != aim_down) {
        cur_angle_down += change_down;
        if (fabs(cur_angle_down - aim_down) <= 3) {
          DuoJi_SetAngle(DOWN, aim_down);
          cur_angle_down = aim_down;
        } else {
          DuoJi_SetAngle(DOWN, cur_angle_down);
        }
      } 
      /**********************************************************************/
      if (change_up != 0) {
        cur_angle_up += change_up;
        aim_up = cur_angle_up;
        DuoJi_SetAngle(UP, cur_angle_up);
      }
      if (change_down != 0) {
        cur_angle_down += change_down;
        aim_down = cur_angle_down;
        DuoJi_SetAngle(DOWN, cur_angle_down);
      }
      /**********************************************************************/
      cnt = 0;
    }
  } else if (htim == &htim5) {
    static uint8_t cnt = 0;
    cnt++;
    float mid = MID_H;
    if (cnt >= 15) {
      float result_x = Track_Blob_PID_X();
      float result_y = Track_Blob_PID_Y();
      DuoJi_SetAngle(DOWN, result_x);
      DuoJi_SetAngle(UP, result_y);
      cnt = 0;
      printf("%f, %f\r\n", target_y, mid);
    }
  }
}

